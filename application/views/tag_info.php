          <span>Boğaziçi Üniversitesi Yöneylem Araştırma Kulübü (BÜYAK) 1982 yılında endüstri mühendisliği öğrencilerinin kişisel ve akademik gelişimlerine destek olmak amacıyla kurulmuş bir öğrenci kulübüdür. BÜYAK, her yıl gerçekleştirdiği etkinliklerle hem üyelerini hem de etkinlik katılımcılarını profesyonel yaşama hazırlamakta,aynı zamanda onlara diğer endüstri mühendisleriyle ulusal ve uluslar arası platformda iletişim ve etkileşim imkanı sunmaktadır.

BÜYAK, Türkiye’deki birçok öğrenci kulübünden farklı olarak, ulusal ve uluslar arası düzeyde seçkin topluluklara üyedir. Bu toplulukların başında ESTIEM (European Students of Industrial Engineering and Management) ve EMT (Endüstri Mühendisliği Toplulukları Birliği) gelmektedir. Kulübümüz, ESTIEM ve EMT platformlarında Boğaziçi Üniversitesi’ni başarıyla temsil etmekte, üyelerinin vizyonunu genişletirken kişisel gelişimlerine katkıda bulunmaktadır.

BÜYAK, endüstri mühendisliği odaklı bir öğrenci kulübü olmasına rağmen, gerçekleştirdiği organizasyonlarla geniş bir kitleye hitap etmekte, gerek üyeleri gerekse organizasyon katılımcıları arasında birçok farklı bölümden öğrenciyi barındırmaktadır.

Sürekli gelişen ve her zaman en iyiyi hedef olarak belirlemiş olan BÜYAK ailesi size , başarılı organizasyonlar, sınırsız kişisel gelişim fırsatı, eğlenceli bir üniversite hayatı ve sıkı bir arkadaşlık ortamı sunuyor.</span>

